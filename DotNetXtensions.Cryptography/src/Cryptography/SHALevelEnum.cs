namespace DotNetXtensions.Cryptography
{
	/// <summary>
	/// An enumeration of SHA encryption levels for generating SHA hashes.
	/// </summary>
	public enum SHALevel
	{
		/// <summary>
		/// The SHA1 hash algorithm.
		/// </summary>
		SHA1,
		/// <summary>
		/// The SHA256 hash algorithm.
		/// </summary>
		SHA256,
		/// <summary>
		/// The SHA384 hash algorithm.
		/// </summary>
		SHA384,
		/// <summary>
		/// The SHA512 hash algorithm.
		/// </summary>
		SHA512
	}
}
